
package clase12;

public class CicloForEach {
    
    public static void main(String[] args) {
        
        String[] ciudades = {"Santiago", "Concepcion", "Antofagasta", "Talca", "Punta Arenas"};
    
        for (String e : ciudades) {
            System.out.println(e);
        }
  
    }
    
}
